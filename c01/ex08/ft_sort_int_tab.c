void	ft_rev_int_tab(int *tab, int size)
{
	int		a;
	int		i;
	int		c;

	i = 0;
	c = 1;
	while (c == 1)
	{
		i = 0;
		c = 0;
		while (i < size - 1)
		{
			if (tab[i] > tab[i + 1])
			{
				a = tab[i];
				tab[i] = tab[i + 1];
				tab[i + 1] = a;
				c = 1;
			}
			i++;
		}
	}
}
