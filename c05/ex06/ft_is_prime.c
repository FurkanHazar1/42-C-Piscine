int	ft_is_prime(int nb)
{
	int	cont;

	cont = 2;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	while (cont <= nb / 2)
	{
		if (nb % cont == 0)
			return (0);
		cont++;
	}
	return (1);
}
